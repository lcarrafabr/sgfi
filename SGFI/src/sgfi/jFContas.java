       public void getGrandeContas() throws ParseException{
       //Consultar pedidos
        Contas oConta = new Contas();

        if ( bancojTextField.getText().length() > 0 ){
            oConta.setBanco(bancojTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"CÃ³digo", "Banco", "Igreja","Nome da conta", "AgÃªncia", "Conta"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        contajTable.setModel(modeloTabela);
        contajTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        contajTable.getColumnModel().getColumn(1).setPreferredWidth(100);
        contajTable.getColumnModel().getColumn(2).setPreferredWidth(150);
        contajTable.getColumnModel().getColumn(3).setPreferredWidth(90);
        contajTable.getColumnModel().getColumn(4).setPreferredWidth(80);
        contajTable.getColumnModel().getColumn(5).setPreferredWidth(80);



try {
            resultSet = oConta.getConsultar();
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Contas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codConta")),
                    resultSet.getString("banco"),
                    resultSet.getString("NomeComunidade"),
                    resultSet.getString("conta"),
                    resultSet.getInt("agencia")+"-"+resultSet.getInt("digitoAgencia"),
                    resultSet.getInt("numeroConta")+"-"+resultSet.getInt("digitoConta"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Comunidade.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
       public void getFieldConta(int codConta) throws ParseException{

        Contas oConta = new Contas();
        ResultSet rs = null;
        oConta.setCodConta(codConta);

        try {
            rs = oConta.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt("codComunidade"), rs.getString("nomeComunidade"));
            comunidadejComboBox.setSelectedItem(oItem);
            //Pegar campos
            codigojSpinner.setValue(rs.getInt("codConta"));
            bancojTextField.setText(rs.getString("banco"));
            agenciaBancojSpinner.setValue(rs.getInt("agencia"));
            digitoAgenciajSpinner.setValue(rs.getInt("digitoAgencia"));
            numeroContajSpinner.setValue(rs.getInt("numeroConta"));
            digitoContajTextField.setText(rs.getString("digitoConta"));
            ativojComboBox.setSelectedItem(rs.getString("ativo"));
            contajTextField.setText(rs.getString("conta"));
            numeroBancojSpinner.setValue(rs.getInt("numeroBanco"));
            observacaojTextArea.setText(rs.getString("observacao"));

        } catch (SQLException ex) {
            Logger.getLogger(Contas.class.getName()).log(Level.SEVERE, null, ex);
        }



    }



