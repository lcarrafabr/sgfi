    private void imprimirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirjButtonActionPerformed
        // TODO add your handling code here:
try {
                Pessoas oPessoa = new Pessoas();

                String caminhoRelJasper = "resources/jRPessoas.jasper";

//                if (!todosjCheckBox.isSelected()){
//                oPessoa.setTipoCliente(tipoClientejComboBox.getSelectedItem().toString());
//                }

                ResultSet rs;

            try {

                rs = oPessoa.getConsultar();


                JRResultSetDataSource ds = new JRResultSetDataSource(rs);

                URL arquivo = getClass().getResource(caminhoRelJasper);

                if (arquivo == null)
                JOptionPane.showMessageDialog(null,"Arquivo jasper n√£o encontrado");
                JasperReport report;
            try {

                    report = JasperManager.loadReport(arquivo.openStream());
                    JasperPrint jasperPrint = JasperFillManager.fillReport(report, new HashMap(), ds);
                    JasperViewer jrviewer = new JasperViewer(jasperPrint, false);
                    jrviewer.setVisible(true);


            } catch (JRException ex) {
                Logger.getLogger(jRPessoas.class.getName()).log(Level.SEVERE, null, ex);
            }

            } catch (IOException ex) {
                Logger.getLogger(jRPessoas.class.getName()).log(Level.SEVERE, null, ex);
            }


            } catch (SQLException ex) {
               Logger.getLogger(jRPessoas.class.getName()).log(Level.SEVERE, null, ex);
            }

    }//GEN-LAST:event_imprimirjButtonActionPerformed

