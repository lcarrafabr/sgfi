    public void getFieldComunidade(int codComunidade) throws ParseException{

        Comunidade oComunidade = new Comunidade();
        ResultSet rs = null;
        oComunidade.setCodComunidade(codComunidade);

        try {
            rs = oComunidade.getConsultar();
            rs.next();

            //Pegar campos
            codComunidadejSpinner.setValue(rs.getInt("codComunidade"));
            nomeComunidadejTextField.setText(rs.getString("nomeComunidade"));
            cnpjjFormattedTextField.setText(rs.getString("cnpj"));
            insc_EstadualjTextField.setText(rs.getString("insc_Estadual"));
            enderecojTextField.setText(rs.getString("endereco"));
            bairrojTextField.setText(rs.getString("bairro"));
            cidadejTextField.setText(rs.getString("cidade"));
            cepjFormattedTextField.setText(rs.getString("cep"));
            ufjComboBox.setSelectedItem(rs.getString("uf"));
            telefone1jFormattedTextField.setText(rs.getString("telefone1"));
            telefone2jFormattedTextField.setText(rs.getString("telefone2"));
            emailjTextField.setText(rs.getString("email"));
            observacaojTextArea.setText(rs.getString("observacao"));

        } catch (SQLException ex) {
            Logger.getLogger(Comunidade.class.getName()).log(Level.SEVERE, null, ex);
        }



    }


