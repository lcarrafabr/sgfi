    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Comunidade oComunidade = new Comunidade();
        // Verifica se o campo Nome da igreja ou comunidade esta preechido
        if(nomeComunidadejTextField.getText().equals("")){

            JOptionPane.showMessageDialog(null, "Preencha o campo: Nome da Igreja ou Comunidade.", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
           // Se o campo nome da igreja estiver preencido conferir o campo UF
        }else if(ufjComboBox.getSelectedItem().equals("...")){

            JOptionPane.showMessageDialog(null, "Preencha o campo: UF.", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);

        }else{
            try {
                salvarjButton.setEnabled(false);
                alterarjButton.setEnabled(false);
                excluirjButton.setEnabled(false);
                editarjButton.setEnabled(false);
                novojButton.setEnabled(true);
                oComunidade.setNomeComunidade(nomeComunidadejTextField.getText());
                oComunidade.setCnpj(cnpjjFormattedTextField.getText());
                oComunidade.setInsc_Estadual(insc_EstadualjTextField.getText());
                oComunidade.setEndereco(enderecojTextField.getText());
                oComunidade.setBairro(bairrojTextField.getText());
                oComunidade.setCidade(cidadejTextField.getText());
                oComunidade.setCep(cepjFormattedTextField.getText());
                oComunidade.setUf((String) ufjComboBox.getSelectedItem());
                oComunidade.setTelefone1(telefone1jFormattedTextField.getText());
                oComunidade.setTelefone2(telefone2jFormattedTextField.getText());
                oComunidade.setEmail(emailjTextField.getText());
                oComunidade.setObervacao(observacaojTextArea.getText());

                
                
                oComunidade.setCadastrar();
                try {
                    ClassConecta.con.commit();
                    ClassConecta.con.setAutoCommit(true);
                } catch (SQLException ex) {
                    Logger.getLogger(jFComunidades.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    getGrandeComunidade();
                    int cod_Comunidade = oComunidade.getId();
                    Funcoes.getItemJTable(cod_Comunidade, gradeComunidadejTable);
                } catch (ParseException ex) {
                    Logger.getLogger(jFComunidades.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(jFComunidades.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

                        
    }//GEN-LAST:event_salvarjButtonActionPerformed

