      public void getGrandeComunidade() throws ParseException{
       //Consultar pedidos
        Comunidade oComunidade = new Comunidade();

        if ( nomeComunidadejTextField.getText().length() > 0 ){
            oComunidade.setNomeComunidade(nomeComunidadejTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Código", "Comunidade", "Endereço", "Telefone 1"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeComunidadejTable.setModel(modeloTabela);
        gradeComunidadejTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        gradeComunidadejTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        gradeComunidadejTable.getColumnModel().getColumn(2).setPreferredWidth(300);
        gradeComunidadejTable.getColumnModel().getColumn(3).setPreferredWidth(100);



try {
            resultSet = oComunidade.getConsultar();
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Comunidade.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codComunidade")),
                    resultSet.getString("nomeComunidade"),
                    resultSet.getString("endereco") + " " + ","+" "+ resultSet.getString("bairro")+ " " + ","+" "+ resultSet.getString("Cidade")
                    + " " + "-"+" "+ resultSet.getString("uf") ,
                    resultSet.getString("telefone1"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Comunidade.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

